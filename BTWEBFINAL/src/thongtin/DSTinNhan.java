package thongtin;

public class DSTinNhan {
	String mathongbao;
	String tenthongbao;
	String noidung;
	String nguoigui;
	String thoigian;
	String nguoinhan;
	String trangthai;
	public DSTinNhan(String mathongbao,String tenthongbao,String noidung,String nguoigui,String thoigian,String nguoinhan,String trangthai)
	{
		this.mathongbao=mathongbao;
		this.tenthongbao=tenthongbao;
		this.noidung=noidung;
		this.nguoigui=nguoigui;
		this.thoigian=thoigian;
		this.nguoinhan=nguoinhan;
		this.trangthai=trangthai;
	}
	public String getMathongbao() {
		return mathongbao;
	}
	public void setMathongbao(String mathongbao) {
		this.mathongbao = mathongbao;
	}
	public String getTenthongbao() {
		return tenthongbao;
	}
	public void setTenthongbao(String tenthongbao) {
		this.tenthongbao = tenthongbao;
	}
	public String getNoidung() {
		return noidung;
	}
	public void setNoidung(String noidung) {
		this.noidung = noidung;
	}
	public String getNguoigui() {
		return nguoigui;
	}
	public void setNguoigui(String nguoigui) {
		this.nguoigui = nguoigui;
	}
	public String getThoigian() {
		return thoigian;
	}
	public void setThoigian(String thoigian) {
		this.thoigian = thoigian;
	}
	public String getNguoinhan() {
		return nguoinhan;
	}
	public void setNguoinhan(String nguoinhan) {
		this.nguoinhan = nguoinhan;
	}
	public String getTrangthai() {
		return trangthai;
	}
	public void setTrangthai(String trangthai) {
		this.trangthai = trangthai;
	}
	
}
